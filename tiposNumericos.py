#! /usr/bin/env python3
# tiposNumericos.py - Tipos numericos.

soma = 3 + 4        # Soma   
print("Soma 3 + 4: {}".format(soma))

sub = 3 - 5         # Subtração  
print("Subtração 3 - 5: {}".format(sub))

mult = 3 * 4        # Multilicação   
print("Multilicação 3 - 4: {}".format(mult))

div = 8 / 5         # Divisão   
print("Divisão 8 / 5: {}".format(div))

trunca = 8 // 5     # Valor inteiro da divisão   
print("Valor inteiro da divisão 8 // 5: {}".format(trunca))

potencia = 6 ** 3   # Potenciação   
print("Potenciação 6 ** 3: {}".format(potencia))

resto = 3 % 4        # Resto   
print("Resto 3 % 4: {}".format(resto))